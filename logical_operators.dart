main(List<String> arguments){

  /*Logical operators { !, ||, &&}*/

  int num1 = 100;
  int num2 = 3;

  if( !(num1 == 10) || (num2 > 0) ){    //False or True = True
    print("Hello");

  }
  else{
    print("Bye");
  }



  if( (num1 == 10) && (num2 > 0) ){   //False and True = False
    print("Hello");

  }
  else{
    print("Bye");
  }


}