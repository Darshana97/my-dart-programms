main(List<String> arguments){

  /*Equity & Relational operators { ==, !=, >, <, >=, <= }*/

  double pi = 3.14;
  double gravity = 9.8;

  print(pi == gravity);
  print(pi != gravity);
  print(pi > gravity);
  print(pi < gravity);
  print(pi >= gravity);
  print(pi <= gravity);

  print(pi is bool);
  print(pi is! String);

}